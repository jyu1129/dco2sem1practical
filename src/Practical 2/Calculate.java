public class Calculate {

    static int calculateAverage(int a, int b){
		return (a+b)/2;
	}
	static int calculateAverage(int a, int b, int c){
		return (a+b+c)/3;
	}
	static double calculateAverage(double a, double b){
		return (a+b)/2;
	}
	static double calculateAverage(double a, double b, double c){
		return (a+b+c)/3;
	}
	
}